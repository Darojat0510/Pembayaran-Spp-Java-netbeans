    public ArrayList<spp>getsppList(){
        
   ArrayList<spp> list = new ArrayList<spp>();
   Connection con = getConnection();
   Statement st;
   ResultSet rs;
   
   try {
   st = con.createStatement();
   rs = st.executeQuery("SELECT `nis`, `nama`, `tempatlahir`, `tanggallahir`,`kelas`,`agama`,`gender`,`status`,`alamat`,`image` FROM `datasiswa`");
   
  spp p;
   while(rs.next()){
   p = new spp(
   rs.getString("nis"),
   rs.getString("nama"),
   rs.getString("tempatlahir"),
   rs.getString("tanggallahir"),
   rs.getString("kelas"),
   rs.getString("agama"),
   rs.getString("gender"),
   rs.getString("status"), 
   rs.getString("alamat"),
   rs.getBytes("image")
  
   );
   list.add(p);
   }
   
   } catch (SQLException ex) {
   Logger.getLogger(pglsiswa.class.getName()).log(Level.SEVERE, null, ex);
   }
   return list;
   }
